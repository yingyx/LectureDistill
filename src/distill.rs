#[cfg(test)]
mod tests {
    use super::*;

    fn mk_concept(
        name: &str,
        level: KeepLevel,
        score: f64,
        rationale: Option<&str>,
    ) -> RankedConcept {
        RankedConcept {
            name: name.into(),
            keep_level: level,
            relevance_score: score,
            source_headings: Vec::new(),
            timestamp_references: Vec::new(),
            rationale: rationale.map(String::from),
        }
    }

    #[test]
    fn deterministic_distill_produces_expected_sections() {
        let content = "# Introduction\nSome text\n";
        let concepts = vec![
            mk_concept(
                "Key Concept A",
                KeepLevel::MustKeep,
                0.9,
                Some("Important topic"),
            ),
            mk_concept("Supporting B", KeepLevel::Compress, 0.7, None),
        ];
        let output = deterministic_distill(content, &concepts);

        assert!(output.contains("# Distilled Lecture Notes (Exam Review)"));
        assert!(output.contains("*Auto-generated by lecture-distill - deterministic mode*"));
        assert!(output.contains("## Key Concepts (Must Know)"));
        assert!(output.contains("## Supporting Concepts (Understand)"));
        assert!(output.contains("## Content Summary"));
        assert!(output.contains("**Key Concept A** - *Important topic*"));
        assert!(output.contains("**Supporting B**"));
    }

    #[test]
    fn deterministic_distill_shifts_heading_levels() {
        let content = "# Title\n## Section\n### Sub-section\nSome text\n";
        let output = deterministic_distill(content, &[]);

        assert!(output.contains("## Title\n"));
        assert!(output.contains("### Section\n"));
        assert!(output.contains("#### Sub-section\n"));

        // Level 4+ headings should be excluded (not shifted).
        let content2 = "# A\n#### Deep\n";
        let output2 = deterministic_distill(content2, &[]);
        assert!(output2.contains("## A\n"));
        assert!(!output2.contains("Deep"));
    }

    #[test]
    fn deterministic_distill_with_empty_concepts_still_produces_structure() {
        let content = "# Lecture 1\nContent here\n## Topic A\nMore content\n";
        let output = deterministic_distill(content, &[]);

        assert!(output.contains("# Distilled Lecture Notes (Exam Review)"));
        assert!(output.contains("## Key Concepts (Must Know)"));
        assert!(output.contains("*No must-know concepts identified.*"));
        assert!(output.contains("## Supporting Concepts (Understand)"));
        assert!(output.contains("*No supporting concepts identified.*"));
        assert!(output.contains("## Content Summary"));
        assert!(output.contains("## Lecture 1"));
        assert!(output.contains("### Topic A"));
    }
}
